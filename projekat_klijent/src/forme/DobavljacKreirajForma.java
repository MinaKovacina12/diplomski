    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {
            int pib = Integer.parseInt(txtPIB.getText());
            String nazivDob = txtNazivDobavljaca.getText();
            String adresaDob = txtAdresaDobavljaca.getText();
            String brojDob = txtBrojDobavljaca.getText();
            String emailDob = txtEmailDobavljaca.getText();

            if (txtPIB.getText().isEmpty()) {
                throw new Exception("Niste uneli PIB");
            }

            if (nazivDob.isEmpty()) {
                throw new Exception("Niste uneli naziv dobavljača");
            }

            if (adresaDob.isEmpty()) {
                throw new Exception("Niste uneli adresu dobavljača");
            }

            if (brojDob.isEmpty()) {
                throw new Exception("Niste uneli broj dobavljača");
            }

            if (emailDob.isEmpty()) {
                throw new Exception("Niste uneli email dobavljača");
            }
           

            if (this.dobavljac == null) {
                Dobavljac dobavljac= new Dobavljac(pib, nazivDob, adresaDob, emailDob, brojDob, new ArrayList<>());
                ClientController.getInstanca().kreirajDobavljaca(dobavljac);
                JOptionPane.showMessageDialog(rootPane, "Sistem je kreirao novog dobavljača");
            } else {
                
                
                dobavljac.setNazivDobavljaca(nazivDob);
                dobavljac.setAdresaDobavljaca(adresaDob);
                dobavljac.setBrojDobavljac(brojDob);
                dobavljac.setEmailDobavljaca(emailDob);
                
                ClientController.getInstanca().zapamtiDobavljaca(dobavljac);
                
                JOptionPane.showMessageDialog(rootPane, "Uspesno ste izmenili dobavljača");
            }

            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Sistem ne može da sacuva dobavljača");
        }

    }//GEN-LAST:event_btnSacuvajActionPerformed

