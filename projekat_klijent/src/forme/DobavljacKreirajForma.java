    private void prepareView() {
        if (dobavljac != null) {
            try {
                
                txtPIB.setText("" + dobavljac.getPIB());
                txtPIB.setEnabled(false);
                txtNazivDobavljaca.setText(dobavljac.getNazivDobavljaca());
                txtAdresaDobavljaca.setText(dobavljac.getAdresaDobavljaca());
                txtEmailDobavljaca.setText(dobavljac.getEmailDobavljaca());
                txtBrojDobavljaca.setText(dobavljac.getBrojDobavljac());
            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Sistem ne može da učita dobavljača", "Greška", JOptionPane.ERROR_MESSAGE);
                dispose();
            }
        }

    }
