    private void validacija(String kriterijum) throws Exception {
        if (kriterijum.equals("")) {
            throw new Exception("Unesite uslov za pretragu!");
        }
    }

    private List<Dobavljac> pretrazi(String kriterijum) throws Exception {
        List<Dobavljac> dobavljaci = ClientController.getInstanca().pretraziDobavljace(kriterijum);
        if (dobavljaci.isEmpty()) {
            throw new Exception("Ne postoje dobavljači koji zadovoljavaju zadati kriterijum!");
        }
        return dobavljaci;
    }
    
    public void osvezi(Dobavljac dobavljac) {
        ModelTabeleDobavljaci model = (ModelTabeleDobavljaci) tblDobavljac.getModel();
        model.obrisiDobavljaca((Dobavljac) Sesija.getInstanca().vratiParametar("Dobavljac"));
        Sesija.getInstanca().obrisiParametar("Dobavljac");
        if (dobavljac != null) {
            Sesija.getInstanca().dodajParametar("Dobavljac", dobavljac);
            model.dodajDobavljaca(dobavljac);
        }
    }

    private void pripremiFormu(List<Dobavljac> listaDobavljaca) {
        try {
            if (listaDobavljaca.isEmpty()) {
                listaDobavljaca = ClientController.getInstanca().prikaziDobavljace();
                btnPonisti.setEnabled(false);
            }
            ModelTabeleDobavljaci model = new ModelTabeleDobavljaci(listaDobavljaca);
            tblDobavljac.setModel(model);
            Sesija.getInstanca().dodajParametar("Lista dobavljaca", model.getDobavljaci());
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
