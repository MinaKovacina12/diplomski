    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed
        // TODO add your handling code here:
        try {
            int red = tblDobavljac.getSelectedRow();
            if (red == -1) {
                JOptionPane.showMessageDialog(rootPane, "Sistem ne može da prikaže podatke o dobavljaču");
                return;
            }
            Dobavljac dob = ((ModelTabeleDobavljaci)tblDobavljac.getModel()).vratiDobavljaca(red);
            JOptionPane.showMessageDialog(rootPane, "Sistem je učitao dobavljača");
            new DobavljacKreirajForma(null, true, dob).setVisible(true);

        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPretraziActionPerformed
        try {
            String kriterijum = txtStringTekst.getText();
            if (kriterijum.isEmpty()) {
                throw new Exception("Niste uneli kriterijum pretrage");
            }

            validacija(kriterijum);

            List<Dobavljac> listaDobavljaca = pretrazi(kriterijum);

            pripremiFormu(listaDobavljaca);
            JOptionPane.showMessageDialog(this, "Sistem je pronašao tražene dobavljače");
            btnPonisti.setEnabled(true);
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Sistem ne može da nađe dobavljacče po zadatoj vrednosti");
        }


    }//GEN-LAST:event_btnPretraziActionPerformed

