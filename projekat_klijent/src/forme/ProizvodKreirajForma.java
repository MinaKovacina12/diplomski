    private void prepareView() {
        if (proizvod != null) {
            try {
                txtNazivProizvoda.setText(proizvod.getNazivProizvoda());
                txtNazivProizvoda.setEnabled(false);
                txtOpis.setText(proizvod.getOpis() + "");
            } catch (Exception ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Sistem ne može da učita proizvod", "Greška", JOptionPane.ERROR_MESSAGE);
                dispose();
            }
        }
    }
