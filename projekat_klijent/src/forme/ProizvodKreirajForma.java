    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        try {
            String nazivProizvoda = txtNazivProizvoda.getText();
            String opis = txtOpis.getText();

            if (nazivProizvoda.isEmpty()) {
                throw new Exception("Niste uneli naziv proizvoda");
            }
            if (opis.isEmpty()) {
                throw new Exception("Niste uneli opis proizvoda");
            }

            if (this.proizvod == null) {
                Proizvod proizvod = new Proizvod(0, nazivProizvoda, opis);

                ClientController.getInstanca().kreirajProizvod(proizvod);
                JOptionPane.showMessageDialog(rootPane, "Uspešno ste kreirali proizvod");
            } else {
                this.proizvod.setOpis(opis);
                ClientController.getInstanca().zapamtiProizvod(this.proizvod);
                JOptionPane.showMessageDialog(rootPane, "Uspešno ste izmenili proizvod");
            }

            //Proizvod proizvod = new Proizvod(0, nazivProizvoda, opis);
            // ClientController.getInstanca().kreirajProizvod(proizvod);
            // JOptionPane.showMessageDialog(rootPane, "Uspesno ste sacuvali proizvod");
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Sistem ne može da sačuva proizvod");
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

