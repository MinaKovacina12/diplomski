    private void popuniKombo() {
        try {
            List<Proizvod> proizvodi = ClientController.getInstanca().prikaziProizvode();
            for (Proizvod proizvod : proizvodi) {
                cmbProizvod.addItem(proizvod);
                
            }
            
            List<Dobavljac> dobavljaci = ClientController.getInstanca().prikaziDobavljace();
            for (Dobavljac dobavljac : dobavljaci) {
                cmbDobavljac.addItem(dobavljac);
                
            }

            cmbProizvod.setSelectedIndex(-1);
            cmbDobavljac.setSelectedIndex(-1);
        } catch (Exception ex) {
          ex.printStackTrace();
          JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }
