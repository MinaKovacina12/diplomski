    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        // TODO add your handling code here:
        try {

            Proizvod proizvod = (Proizvod) cmbProizvod.getSelectedItem();
            if (proizvod == null) {
                throw new Exception("Morate izabrati neki proizvod!");
            }

            Dobavljac dobavljac = (Dobavljac) cmbDobavljac.getSelectedItem();
            if (dobavljac == null) {
                throw new Exception("Morate izabrati nekog dobavljača!");
            }

            double cena = Double.parseDouble(txtCena.getText());
            if (txtCena.getText().isEmpty()) {
                throw new Exception("Niste uneli cenu");
            }

            if(cena<=0){
                throw new Exception("Cena mora biti veća od 0");
            }
            
            Cenovnik cenovnik = new Cenovnik(dobavljac, proizvod, cena);
            
            ClientController.getInstanca().kreirajCenovnik(cenovnik);
            JOptionPane.showMessageDialog(rootPane, "Uspešno ste kreirali cenovnik");
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

